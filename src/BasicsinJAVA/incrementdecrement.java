package BasicsinJAVA;

public class incrementdecrement {
    public static void main(String[] args) {

        int x = 5; // initial value

        System.out.println(++x); // pre-increment:increase by 1, then print
        System.out.println(x++); // post-increment: prints 6, then x increase by 1

        System.out.println(x);

        System.out.println(--x); // pre-decrement: x becomes 6, prints 6
        System.out.println(x--); // post-decrement: prints 6, then x becomes 5

        System.out.println(x);




    }
}
